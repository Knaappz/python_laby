import math
def suma(a,b):
    c = a + b
    return c
def roz(a,b):
    c = a - b
    return c
def mn(a,b):
    c = a * b
    return c
def dz(a,b):
    c = a / b
    return c
def pier(a,b):
    c = math.sqrt(a)
    d = math.sqrt(b)
    return c, d

print(suma(5,7))
print(roz(5,7))
print(mn(5,7))
print(dz(5,7))
print(pier(5,7))
